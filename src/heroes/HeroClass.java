package heroes;

public class HeroClass {
	public int health;
	public int strength;
	public int magic;
	public int dodge;
	
	public HeroClass(int startHealth, int startStrength, int startMagic, int startDodge){
				health = startHealth;
				strength = startStrength;
				magic = startMagic;
				dodge = startDodge;
		}
		
}
